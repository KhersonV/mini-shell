#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	leng_src;
	size_t	i;

	i = 0;
	leng_src = ft_strlen(src);
	if (size == 0)
		return (leng_src);
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (leng_src);
}
